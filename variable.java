package Firstpackege;

public class variable {

	
	int A=10;
	char B='Q';
	float C=0.123f;
	double D=3.14;
	long E=10000000L;
	boolean F= false;
	short G=20;
	
	public static void main(String[] args) {
	
		variable obj= new variable();
		
    System.out.println("int: " + obj.A);

    System.out.println("char: " + obj.B);

    System.out.println("float: " + obj.C);

    System.out.println("double: " +obj.D);

    System.out.println("long: " +obj.E);
    System.out.println("boolean: " +obj.F);
    System.out.println("short: " +obj.G);


	}
	

}